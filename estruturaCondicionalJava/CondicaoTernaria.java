
public class CondicaoTernaria {

	public static void main(String[] args) {
		// Expressão condicional ternária
		// sintaxe: (condição) ? valor_caso_seja_verdadeiro : valor_caso_seja_falso
		
		double preco = 35.5;
		String desconto = (preco > 20.0) ? "teste1" : "teste2";
		System.out.println(desconto);
		
		
	}

}
