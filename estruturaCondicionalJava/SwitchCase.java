
public class SwitchCase {

	public static void main(String[] args) {
		// ESTRUTURA DO SWITCH CASE
		
		int x = 2;
		
		switch(x) {
		case 1:
			System.out.println("Case 1");
			break;
		case 2:
			System.out.println("Case 2");
			break;
		case 10:
			System.out.println("Case 10");
			break;
		default:
			break;
		}
	

	}

}
